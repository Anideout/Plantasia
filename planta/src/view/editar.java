    private void configurarTabla(){
        tbnTabla.addMouseListener(new java.awt.event.MouseAdapter() {
            public void moseClicked(java.awt.event.MouseEvent evt ){
                int filaSeleccionada = tbnTabla.getSelectedRow();
                if(filaSeleccionada >= 0){
                    //obtener los valores del objeto
                    String rut = (String) tbnTabla.getValueAt(filaSeleccionada, 0);
                    String nombre = (String) tbnTabla.getValueAt(filaSeleccionada, 1);
                    String apellido = (String) tbnTabla.getValueAt(filaSeleccionada, 2);
                    int edad = (int) tbnTabla.getValueAt(filaSeleccionada, 3);
                    String calle = (String) tbnTabla.getValueAt(filaSeleccionada, 4);
                    String comuna = (String) tbnTabla.getValueAt(filaSeleccionada, 5);
                    int numero = (int) tbnTabla.getValueAt(filaSeleccionada, 6);
                    String nombrePlanta = (String) tbnTabla.getValueAt(filaSeleccionada, 7);
                    int precio = (int) tbnTabla.getValueAt(filaSeleccionada, 8);
                    
                    txtRut.setText(rut);
                    txtNombre.setText(nombre);
                    txtApellido.setText(apellido);
                    txtEdad.setText(String.valueOf(edad));
                    txtCalle.setText(calle);
                    txtComuna.setText(comuna);
                    txtPlanta.setText(nombrePlanta);
                    txtPrecio.setText(String.valueOf(precio));
                    txtNumero.setText(String.valueOf(numero));
                    
                    //inhabilitar el campo rut
                    txtRut.setEnabled(false);
                }
                
            }
        });
    }
    
    
    
    
