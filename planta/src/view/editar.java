    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        
        int filaSeleccionada = tbnTabla.getSelectedRow();
        if(filaSeleccionada >= 0){
            String rut = txtRut.getText();
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String calle = txtCalle.getText();
            String comuna = txtComuna.getText();
            String nombrePlanta = txtPlanta.getText();
            String precio = txtPrecio.getText();
            int seguimiento =  (int) nSeguimiento();
            int edad = 0;
            try {
                edad = Integer.parseInt(txtEdad.getText());
            }catch (Exception e){
                JOptionPane.showMessageDialog(this,"el campo apellido no puede venir vacio!!");
                return;
            }
            int numero = 0;
            try {
                numero = Integer.parseInt(txtNumero.getText());
            }catch (Exception e){
                JOptionPane.showMessageDialog(this,"el campo numero no puede venir vacio!!");
                return;
            }
            int precios = 0;
            try {
                precios = Integer.parseInt(txtPrecio.getText());
            }catch (Exception e){
                JOptionPane.showMessageDialog(this,"el campo Precio no puede venir vacio!!");
                return;
            }
            Direccion direccionModificada = new Direccion(calle, comuna, numero);
            Planta plantaModificada = new Planta(nombrePlanta, precios, seguimiento);
            Persona personaModificada = new Persona(rut, nombre, apellido, edad, direccionModificada, plantaModificada);
            gestionPersona.modificarPersona(filaSeleccionada, personaModificada);
            
            //actualizar la tabla
            tbnTabla.setValueAt(rut, filaSeleccionada, 0);
            tbnTabla.setValueAt(nombre, filaSeleccionada, 1);
            tbnTabla.setValueAt(apellido, filaSeleccionada, 2);
            tbnTabla.setValueAt(edad, filaSeleccionada, 3);
            tbnTabla.setValueAt(calle, filaSeleccionada, 4);
            tbnTabla.setValueAt(comuna, filaSeleccionada, 5);
            tbnTabla.setValueAt(numero, filaSeleccionada, 6);
            tbnTabla.setValueAt(nombrePlanta, filaSeleccionada, 7);
            tbnTabla.setValueAt(precio, filaSeleccionada, 8);
            tbnTabla.setValueAt(seguimiento, filaSeleccionada, 9);
        }


        //boton aceptar, y validar los datos antes de enviarlos
        try{
            //ingresar el nombre del producto
            if(txtNombre.getText().trim().length() == 0){
               
                return;
            }
            if(txtApellido.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this,"el campo apellido no puede venir vacio!!");
                return;
            }
            if(txtCalle.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "el campo calle no puede venir vacio!!");
                return;
            }
            if(txtPlanta.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "el campo Planta no puede venir vacio!!");
                return;
            }
            if(txtComuna.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "el campo comuna no puede venir vacio!!");
                return;
            }
            this.setVisible(false);
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(this,"los datos ingresados no son validos!");
            return;
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

